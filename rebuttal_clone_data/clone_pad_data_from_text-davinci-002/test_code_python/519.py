def solution():
    regular_croissant_cost = 3.50
    almond_croissant_cost = 5.50
    croissants_per_week = 2
    weeks_per_year = 52
    total_cost = (regular_croissant_cost + almond_croissant_cost) * croissants_per_week * weeks_per_year
    result = total_cost
    return result

print(solution())