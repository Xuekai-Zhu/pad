def solution():
    model_cars = 5
    bottles_of_paint = 5
    paintbrushes = 5
    cost_of_model_car = 20
    cost_of_bottle_of_paint = 10
    cost_of_paintbrush = 2
    total_spent = (model_cars * cost_of_model_car) + (bottles_of_paint * cost_of_bottle_of_paint) + (paintbrushes * cost_of_paintbrush)
    result = total_spent
    return result

print(solution())