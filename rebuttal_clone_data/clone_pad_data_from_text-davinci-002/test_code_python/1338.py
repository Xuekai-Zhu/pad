def solution():
    small_ceiling_lights = 10
    medium_ceiling_lights = 2
    large_ceiling_lights = 4
    small_bulbs = small_ceiling_lights
    medium_bulbs = medium_ceiling_lights * 2
    large_bulbs = large_ceiling_lights * 3
    total_bulbs = small_bulbs + medium_bulbs + large_bulbs
    result = total_bulbs
    return result

print(solution())