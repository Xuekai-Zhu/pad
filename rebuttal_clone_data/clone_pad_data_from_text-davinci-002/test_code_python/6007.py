def solution():
    pumpkin_seed_packets = 3
    tomato_seed_packets = 4
    chili_pepper_seed_packets = 5
    pumpkin_seed_cost = 2.50
    tomato_seed_cost = 1.50
    chili_pepper_seed_cost = 0.90
    total_cost = (pumpkin_seed_packets * pumpkin_seed_cost) + (tomato_seed_packets * tomato_seed_cost) + (chili_pepper_seed_packets * chili_pepper_seed_cost)
    result = total_cost
    return result

print(solution())