def solution():
    matinee_tickets = 200
    evening_tickets = 300
    threeD_tickets = 100
    matinee_price = 5
    evening_price = 12
    threeD_price = 20
    total_revenue = (matinee_tickets * matinee_price) + (evening_tickets * evening_price) + (threeD_tickets * threeD_price)
