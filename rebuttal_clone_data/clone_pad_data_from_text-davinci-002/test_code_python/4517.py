def solution():
    bananas = 4
    apples = 3
    strawberries = 24
    avocados = 2
    grapes = 1
    banana_cost = 1
    apple_cost = 2
    strawberry_cost = 4
    avocado_cost = 3
    grape_cost = 2
    total_cost = (bananas * banana_cost) + (apples * apple_cost) + (strawberries * strawberry_cost) + (avocados * avocado_cost) + (grapes * grape_cost)
