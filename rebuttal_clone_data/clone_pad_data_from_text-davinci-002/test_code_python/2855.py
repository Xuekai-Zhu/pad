def solution():
    total_weeks = 6
    weekly_orange_creamsicle = 2
    weekly_ice_cream_sandwich = 1.5
    weekly_nutty_buddy = 3
    total_spent = (weekly_orange_creamsicle * 3 + weekly_ice_cream_sandwich * 2 + weekly_nutty_buddy * 2) * total_weeks
