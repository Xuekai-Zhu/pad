def solution():
    """Christina has 3 snakes. 1 snake is 2 feet long. Another snake is 16 inches long. The last snake is 10 inches long. How many inches are all of her snakes combined?"""
    snake_1_length = 2
    snake_2_length = 16
    snake_3_length = 10
    total_length = snake_1_length + snake_2_length + snake_3_length
    result = total_length
    return result

print(solution())