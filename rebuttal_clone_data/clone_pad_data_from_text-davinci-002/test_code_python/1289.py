def solution():
    Garys_gold = 30
    Annas_gold = 50
    Garys_gold_cost = 15
    Annas_gold_cost = 20
    total_gold = Garys_gold + Annas_gold
    total_cost = Garys_gold_cost + Annas_gold_cost
    combined_cost = total_gold * total_cost
    result = combined_cost
    return result

print(solution())