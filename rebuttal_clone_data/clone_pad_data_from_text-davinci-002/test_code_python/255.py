def solution():
    """Bush and Matt are brothers. Bush is younger than Matt by 3 years. This year Bush will be 12 years old. What will be Matt's age 10 years from now?"""
    bush_age = 12
    difference_in_age = 3
    matt_age = bush_age + difference_in_age
    result = matt_age + 10
    return result

print(solution())