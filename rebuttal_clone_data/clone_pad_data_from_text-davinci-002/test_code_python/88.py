def solution():
    """Brandon sold 86 geckos last year. He sold twice that many the year before. How many geckos has Brandon sold in the last two years?"""
    geckos_sold_1 = 86
    geckos_sold_2 = 2 * geckos_sold_1
    total_geckos = geckos_sold_1 + geckos_sold_2
    result = total_geckos
    return result

print(solution())