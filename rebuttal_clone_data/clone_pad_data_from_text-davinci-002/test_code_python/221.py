def solution():
    """Mira jogs every morning. She jogs 5 miles per hour. If she jogs for 2 hours every morning, how many miles can she jog for five days?"""
    miles_per_hour = 5
    hours_per_day = 2
    days = 5
    miles_jogged = miles_per_hour * hours_per_day * days
    result = miles_jogged
    return result

print(solution())