def solution():
    initial_speed = 10
    initial_time = 0.5
    second_speed = 20
    second_time = 0.5
    third_speed = 8
    third_time = 0.25
    total_distance = (initial_speed * initial_time) + (second_speed * second_time) + (third_speed * third_time)
    result = total_distance
    return result

print(solution())