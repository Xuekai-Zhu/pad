def solution():
    bonus = 900
    stock_A = bonus / 3
    stock_B = stock_A
    stock_C = stock_A
    stock_A_value = stock_A * 2
    stock_B_value = stock_B * 2
    stock_C_value = stock_C / 2
    total_value = stock_A_value + stock_B_value + stock_C_value
    result = total_value
    return result

print(solution())