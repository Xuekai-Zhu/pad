def solution():
    trout_caught = 4
    trout_weight = 2
    catfish_caught = 3
    catfish_weight = 1.5
    bluegills_caught = 5
    bluegills_weight = 2.5
    total_fish_weight = (trout_caught * trout_weight) + (catfish_caught * catfish_weight) + (bluegills_caught * bluegills_weight)
    result = total_fish_weight
    return result

