def solution():
    dad_marshmallows = 21
    joe_marshmallows = dad_marshmallows * 4
    dad_marshmallows_roasted = dad_marshmallows / 3
    joe_marshmallows_roasted = joe_marshmallows / 2
    total_marshmallows_roasted = dad_marshmallows_roasted + joe_marshmallows_roasted
    result = total_marshmallows_roasted
    return result

print(solution())