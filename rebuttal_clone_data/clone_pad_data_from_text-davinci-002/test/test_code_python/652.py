def solution():
     tulip_bulbs = 20
     iris_bulbs = tulip_bulbs / 2
     daffodil_bulbs = 30
     crocus_bulbs = daffodil_bulbs * 3
     total_bulbs = tulip_bulbs + iris_bulbs + daffodil_bulbs + crocus_bulbs
