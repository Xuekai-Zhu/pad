def solution():
    grooming_time_dogs = 2.5 * 60
    grooming_time_cats = 0.5 * 60
    number_of_dogs = 5
    number_of_cats = 3
    total_grooming_time = (grooming_time_dogs * number_of_dogs) + (grooming_time_cats * number_of_cats)
    result = total_grooming_time
    return result

print(solution())