def solution():
     spam_cost = 3
     peanut_butter_cost = 5
     bread_cost = 2
     amount_spam = 12
     amount_peanut_butter = 3
     amount_bread = 4
     total_cost = (spam_cost * amount_spam) + (peanut_butter_cost * amount_peanut_butter) + (bread_cost * amount_bread)
     result = total_cost
     return result

