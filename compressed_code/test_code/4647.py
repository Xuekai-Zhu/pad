def solution():
    
    pumpkin_price = 2.5
    tomato_price = 1.5
    chili_price = 0.9

    pumpkin_total = pumpkin_price * 3
    tomato_total = tomato_price * 4
    chili_total = chili_price * 5

    total_cost = pumpkin_total + tomato_total + chili_total
    result = total_cost
    return result

print(solution())