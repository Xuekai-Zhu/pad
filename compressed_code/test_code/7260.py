def solution():
    
    danny_watermelons = 3
    danny_slices_per_watermelon = 10
    sister_watermelons = 1
    sister_slices_per_watermelon = 15
    total_slices = (danny_watermelons * danny_slices_per_watermelon) + (sister_watermelons * sister_slices_per_watermelon)
