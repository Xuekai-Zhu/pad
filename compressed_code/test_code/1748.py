def solution():
    
    betty_oranges = 15
    bill_oranges = 12
    combined_oranges = betty_oranges + bill_oranges
    frank_oranges = 3 * combined_oranges
    total_seeds = frank_oranges * 2
    total_trees = total_seeds
    total_oranges = total_trees * 5
    result = total_oranges
    return result

print(solution())