def solution():
    
    gary_grams = 30
    gary_cost_per_gram = 15
    gary_cost = gary_grams * gary_cost_per_gram
    anna_grams = 50
    anna_cost_per_gram = 20
    anna_cost = anna_grams * anna_cost_per_gram
    total_cost = gary_cost + anna_cost
    result = total_cost
    return result

print(solution())