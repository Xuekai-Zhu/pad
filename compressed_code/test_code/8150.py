def solution():
    
    jackie_miles_per_day = 2
    jessie_miles_per_day = 1.5
    total_days = 6
    jackie_total_miles = jackie_miles_per_day * total_days
    jessie_total_miles = jessie_miles_per_day * total_days
    difference = jackie_total_miles - jessie_total_miles
    result = difference
    return result

