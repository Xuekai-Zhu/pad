def solution():
    
    
    
    total_time = 10
    
    
    halfway_time = total_time / 2
    
    
    current_hotdogs = 20
    
    
    hotdogs_left = 75 - current_hotdogs
    
    
    time_left = halfway_time
    
    
    hotdogs_per_minute = hotdogs_left / time_left
    
    result = hotdogs_per_minute
    return result

print(solution())