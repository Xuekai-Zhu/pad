def solution():
    
    drink_cost = 2
    cake_cost = 10
    ice_cream_cost = 5
    num_drinks = 10
    num_cakes = 5
    num_ice_creams = 100
    total_cost = (drink_cost * num_drinks) + (cake_cost * num_cakes) + (ice_cream_cost * num_ice_creams)
    result = total_cost
    return result

