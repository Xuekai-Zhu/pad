def solution():
    
    pies_per_pound = 1/3
    christine_strawberries = 10
    rachel_strawberries = 2 * christine_strawberries
    total_strawberries = christine_strawberries + rachel_strawberries
    total_pies = total_strawberries * pies_per_pound
    result = total_pies
    return result

print(solution())