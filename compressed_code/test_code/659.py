def solution():
    
    apple_trees = 30
    apple_yield = 150
    peach_trees = 45
    peach_yield = 65
    total_apples = apple_trees * apple_yield
    total_peaches = peach_trees * peach_yield
    total_fruit = total_apples + total_peaches
    result = total_fruit
    return result

