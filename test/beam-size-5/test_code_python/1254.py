def solution():
    
    # Define the cost of the main phone plan
    MAIN_PHONE_COST = 20

    # Calculate the cost of the additional phone
    additional_phone_cost = MAIN_PHONE_COST * 2

    # Calculate the total cost of the phone plan
    total_cost = MAIN_PHONE_COST + additional_phone_cost

    # Display the total cost
    result = total_cost
    return result

print(solution())