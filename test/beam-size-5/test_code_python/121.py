def solution():
    
    # Define the initial number of spoons
    initial_spoons = 12

    # Define the number of spoons bought to Julia's husband
    husband_spoons = 5

    # Define the number of spoons used to sample the stew
    stew_spoons = 3

    # Calculate the total number of spoons
    total_spoons = initial_spoons + husband_spoons - stew_spoons

    # Display the total number of spoons
    result = total_spoons
    return result

