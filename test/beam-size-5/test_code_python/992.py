def solution():
    
    # Define Akbar's age and Alessandro's age
    akbar_age = 3
    alessandro_age = 4

    # Calculate the average age of the group
    average_age = 20 / 4

    # Calculate Helene's age
    helene_age = 2 * average_age

    # Calculate the total age of the group
    total_age = akbar_age + alessandro_age + helene_age

    # Calculate the age of Wilfred
    wilfred_age = total_age - 20

    # Display the age of Wilfred
    result = wilfred_age
    return result

