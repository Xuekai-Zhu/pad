def solution():
    # Calculate the total number of cookies Jenny puts in her son's lunch box
    lunch_cookies = 4 * 5

    # Calculate the total number of cookies Jenny eats in 7 days
    husband_cookies = 1 * 7

    # Calculate the total number of cookies Jenny has
    total_cookies = 36 - lunch_cookies - husband_cookies

    # Calculate the number of cookies Jenny eats
    jenny_cookies = total_cookies

    result = jenny_cookies
    return result

print(solution())