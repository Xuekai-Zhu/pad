def solution():
    
    # Define the regular price of each blouse
    REGULAR_PRICE = 20

    # Calculate the discounted price of each blouse
    discounted_price = REGULAR_PRICE * 0.7

    # Calculate the total cost of the discounted blouses
    total_cost = discounted_price * 4

    # Display the total cost
    result = total_cost
    return result

