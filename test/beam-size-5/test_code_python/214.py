def solution():
    harriet_age = 21
    zack_age = harriet_age / 2
    adrian_age = 3 * harriet_age

    # Calculate the total age of the three years
    total_age = harriet_age + zack_age + adrian_age + 3

    # Calculate the average age of the three years
    average_age = total_age / 3
    result = average_age
    return result

print(solution())