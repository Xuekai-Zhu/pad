def solution():
    martin_weight = 55  # Martin's weight is 55 kg
    carl_weight = martin_weight + 16  # Carl's weight is 16 kg more than Martin's weight
    christian_weight = carl_weight + 8  # Christian's weight is 8 kg more than Carl's weight
    harry_weight = christian_weight - 5  # Harry is 5 kg less than Christian's weight

    result = harry_weight
    return result

print(solution())