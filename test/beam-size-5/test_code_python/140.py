def solution():
    # Calculate the total cost of bleach and cloths for Kim
    cost_bleach = 2 * 2  # 2 bottles of bleach each costing $2 each
    cost_cloths = 5 * 5  # 1 pack of cloths each costing $5 each
    total_cost = cost_bleach + cost_cloths

    # Calculate Kim's weekly income
    weekly_income = 92
    profit = weekly_income - total_cost
    result = profit
    return result

