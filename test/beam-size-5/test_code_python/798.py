def solution():
    
    # Define the number of stuffed animals
    puppies = 3
    koalas = 5
    zebras = 2
    frogs = 4

    # Calculate the total number of stuffed animals
    total_animals = puppies + koalas + zebras + frogs

    # Calculate the number of stuffed goats needed
    goats_needed = total_animals * 0.3

    # Display the number of stuffed goats needed
    result = goats_needed
    return result

print(solution())