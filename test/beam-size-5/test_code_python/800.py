def solution():
    
    # Define the speeds of the first car and the second car
    car1_speed = 60
    car2_speed = 70

    # Calculate the distance traveled by the first car
    car1_distance = car1_speed * 2

    # Calculate the distance traveled by the second car
    car2_distance = car2_speed * 2

    # Calculate the total distance traveled
    total_distance = car1_distance + car2_distance

    # Return the result
    result = total_distance
    return result

print(solution())