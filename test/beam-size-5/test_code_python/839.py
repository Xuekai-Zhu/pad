def solution():
    # Calculate the total cost of the squirt guns
    squirt_cost = 6 * 2

    # Calculate the total cost of the water balloons
    water_cost = 3 * 3

    # Calculate the total cost of all the items
    total_cost = squirt_cost + water_cost

    # Calculate the amount of money left
    money_left = 20 - total_cost
