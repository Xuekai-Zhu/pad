def solution():
    mia_age = 16  # Mia is currently 16 years apart in age
    emma_age = 40  # Emma is 40 years old

    # Calculate Mia's age
    mia_age = mia_age - mia_age

    # Calculate Emma's age
    emma_age = emma_age - mia_age

    # Calculate the average of their ages
    average_age = (mia_age + emma_age) / 2
    result = average_age
    return result

print(solution())