def solution():
    
    # Define the number of cows and sheep
    cows = 30
    sheep = 20

    # Calculate the number of cows and sheep for the zoo
    cows_zoo = cows * 2
    sheep_zoo = sheep * 2

    # Calculate the total number of animals
    total_animals = cows_zoo + sheep_zoo

    # Display the total number of animals
    result = total_animals
    return result

print(solution())