def solution():
    
    # Define the initial number of puzzle pieces
    initial_pieces = 500

    # Calculate the number of puzzle pieces placed during the hour
    placed_pieces = initial_pieces / 2

    # Calculate the total number of puzzle pieces placed during the hour
    total_pieces = placed_pieces + 500

    # Display the total number of puzzle pieces placed
    result = total_pieces
    return result

print(solution())