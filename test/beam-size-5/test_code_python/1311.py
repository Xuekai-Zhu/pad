def solution():
    
    # Define the time Ana spends peeling an orange and the time Jane spends doing the same
    ana_time = 3
    jane_time = 4

    # Calculate the number of oranges Ana will have peeled after an hour
    ana_oranges = ana_time / ana_time

    # Calculate the number of oranges Jane will have peeled after an hour
    jane_oranges = jane_time / jane_time

    # Calculate the difference in the number of oranges Ana and Jane will have peeled
    difference = ana_oranges - jane_oranges

    # Display the difference in the number of oranges
    result = difference
    return result

print(solution())