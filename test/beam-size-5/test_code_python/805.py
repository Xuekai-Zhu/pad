def solution():
    
    # Define the initial number of butterscotch candies
    initial_candies = 100

    # Define the number of butterscotch candies sold
    candies_sold = 150 + 100

    # Calculate the remaining number of butterscotch candies to sell
    remaining_candies = initial_candies - candies_sold

    # Display the remaining number of butterscotch candies to sell
    result = remaining_candies
    return result

