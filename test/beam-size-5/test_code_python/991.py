def solution():
    # Define the initial amount of money Adam had
    initial_money = 30

    # Define the amount of money Adam's mother gave him
    mother_money = 6

    # Define the amount of money Adam's father gave him
    father_money = 2 * mother_money

    # Calculate the total amount of money Adam has
    total_money = initial_money + mother_money + father_money

    # Calculate the amount of money Adam has to contribute
    contribute_money = total_money - total_money
    result = contribute_money
    return result

print(solution())