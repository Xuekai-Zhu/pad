def solution():
    # Calculate the number of silver pesos Anna has
    anna_silver_pesos = 2 * 50  # Anna has twice as many silver pesos as he has

    # Calculate the number of gold pesos Anna has
    anna_gold_pesos = 80 + 40  # Anna has 40 more gold pesos than he has

    # Calculate the total number of pesos they have together
    total_pesos = 50 + 80 + anna_silver_pesos + anna_gold_pesos
    result = total_pesos
    return result

