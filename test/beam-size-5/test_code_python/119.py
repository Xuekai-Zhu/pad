def solution():
    # Calculate the distance Rosie can run in 3 hours at 10 miles per hour
    distance_3_hours = 10 * 3

    # Calculate the distance Rosie can run in 5 miles per hour after 3 hours
    distance_5_hours = 5 * 5

    # Calculate the total distance Rosie can run in 7 hours
    total_distance = distance_3_hours + distance_5_hours
    result = total_distance
    return result

print(solution())