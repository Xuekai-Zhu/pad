def solution():
    
    # Define the amount of water each girl got
    girls_water = 24 / 6

    # Define the amount of water the boy got
    boy_water = 6

    # Calculate the total amount of water received
    total_water = girls_water * 2 + boy_water

    # Calculate the amount of water left
    water_left = total_water - total_water

    # Display the amount of water left
    result = water_left
    return result

print(solution())