def solution():
    hourly_rate = 10
    saturday_hours = 2
    sunday_hours = 1

    # Calculate the total number of hours worked by Jill
    jill_hours = saturday_hours + sunday_hours

    # Calculate the total amount earned by John
    john_earnings = jill_hours * hourly_rate * 2

    # Calculate the total amount earned by John
    john_earnings = john_hours * hourly_rate * 3

    # Calculate the difference in earnings between John and Jill
    earnings_difference = john_earnings - jill_earnings
    result = earnings_difference
    return result

print(solution())