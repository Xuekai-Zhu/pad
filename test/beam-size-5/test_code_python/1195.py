def solution():
    
    # Define the initial amount of root beer
    initial_gallons = 24

    # Calculate the amount of root beer consumed on the first day
    first_day_gallons = initial_gallons

    # Calculate the amount of root beer consumed on the second day
    second_day_gallons = 4

    # Calculate the amount of root beer consumed on the third day
    third_day_gallons = 7

    # Calculate the total amount of root beer consumed
    total_gallons = first_day_gallons + second_day_gallons + third_day_gallons

    # Display the total amount of root beer consumed
    result = total_gallons
    return result

