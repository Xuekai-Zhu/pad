def solution():
    
    # Calculate the distance traveled in the first 2 hours
    distance_1 = 60 * 2

    # Calculate the distance traveled in the second 1 hour
    distance_2 = 30 * 1

    # Calculate the total distance traveled
    total_distance = distance_1 + distance_2

    # Calculate the average speed
    average_speed = total_distance / 3

    # Display the average speed
    result = average_speed
    return result

