def solution():
    # Define the cost of a pencil and eraser
    pencil_cost = 1.20
    eraser_cost = 0.30

    # Calculate the combined cost of 8 pens
    combined_cost = pencil_cost + eraser_cost

    # Calculate the cost of 8 pens
    pen_cost = combined_cost / 8
    result = pen_cost
    return result

print(solution())