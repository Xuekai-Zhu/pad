def solution():
    
    # Define the prices of jewelry and electronic gadgets
    jewelry_price = 5000
    electronic_price = 8000

    # Calculate the profit from the jewelry market
    jewelry_profit = jewelry_price * 0.02

    # Calculate the profit from the electronic gadgets market
    electronic_profit = electronic_price * 0.02

    # Calculate the total profit
    total_profit = jewelry_profit + electronic_profit

    # Display the total profit
    result = total_profit
    return result

print(solution())